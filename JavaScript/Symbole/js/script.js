// Die folgenden Werte werden in false konvertiert und verhalten sich deswegen auch wie false:

// undefined, null, 0, -0, NaN, ""  Der leere String.

// Alle anderen Werte, einschließlich aller Objekte (und Arrays), 
// werden in true konvertiert und verhalten sich wie true.

// Der &&-Operator führt die boolesche UND-Verknüpfung durch. Er wird nur dann zu einem 
// wahren Wert ausgewertet, wenn beide Operanden wahre Werte sind. 
// Andernfalls wird er zu einem falschen Wert ausgewertet.

// Der ||-Operator führt eine boolesche ODER-Operation aus: Er wird zu einem wahren Wert ausgewertet, 
// wenn einer (oder beide) Operanden wahre Werte sind. Zu einem falschen Wert wird er nur ausgewertet,
// wenn beide Operanden falsche Werte sind.

// !-Operator, der boolesche NICHT-Operation: Er wird zu true ausgewertet, wenn sein Operand ein falscher Wert ist, 
// und zu false, wenn sein Operand ein wahrer Wert ist.


